package com.ele.parse.entity;

public class ItemPoint {

	private int x;
	private int width;
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getWidth() {
		return width;
	}
	public void setWidth(int width) {
		this.width = width;
	}
	public ItemPoint(int x, int width) {
		super();
		this.x = x;
		this.width = width;
	}
	
	
	
}
